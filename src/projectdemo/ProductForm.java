    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int productId = Integer.parseInt(txtProductId.getText());
    
        // Retrieve updated values from the text fields
        String name = txtProductName.getText();
        String description = txtDescription.getText();
        String barcode = txtBarcode.getText();
        double price = Double.parseDouble(txtPrice.getText());
        int stockQuantity = Integer.parseInt(txtStockQuantity.getText());
        int minQuantity = Integer.parseInt(txtMinQuantity.getText());
        int maxQuantity = Integer.parseInt(txtMaxQuantity.getText());
        int categoryID = Integer.parseInt(txtCategoryID.getText());
        int supplierID = Integer.parseInt(txtSupplierID.getText());
    
        // Parse the EntryDate from a text field (assuming you have a txtEntryDate field)
        Date entryDate = null;
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
        try {
           entryDate = dateFormat.parse(txtEntryDate.getText());
        } catch (ParseException e) {
           // Handle parsing error if necessary
           e.printStackTrace();
        }
    
        // Create a Product object with updated values
        // here I need to use the default Product constructor.
        //Product(String name, String description, String barcode, double price, int stockQuantity,int minQuantity, int maxQuantity, Date entryDate, int categoryID, int supplierID)
        Product _updatedProduct = new Product(productId, name, description, barcode, price, stockQuantity, minQuantity, maxQuantity, entryDate, categoryID, supplierID);
    
        // Call the updateProduct method from ProductData class
        ProductData.updateProduct(_updatedProduct);
    
        // Notify the user that the product has been updated
        JOptionPane.showMessageDialog(this, "Product updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);        
        
        
    }//GEN-LAST:event_btnUpdateActionPerformed

