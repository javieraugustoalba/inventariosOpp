    private void btnFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFindActionPerformed
    // Get the product ID from a text field (assuming you have a txtProductId field)
    int productId = Integer.parseInt(txtProductId.getText());
    
    // Call the findProductById method from ProductData class
    Product product = ProductData.findProductById(productId);
    
    // Check if product is found
    if (product != null) {
        // Populate GUI components with product details
        txtProductName.setText(product.getName());
        txtDescription.setText(product.getDescription());
        txtBarcode.setText(product.getBarcode());
        txtPrice.setText(String.valueOf(product.getPrice()));
        txtStockQuantity.setText(String.valueOf(product.getStockQuantity()));
        txtMinQuantity.setText(String.valueOf(product.getMinQuantity()));
        txtMaxQuantity.setText(String.valueOf(product.getMaxQuantity()));
        txtCategoryID.setText(String.valueOf(product.getCategoryID()));
        txtSupplierID.setText(String.valueOf(product.getSupplierID()));
        
        // Convert the EntryDate to a formatted date string if it's not null
        if (product.getEntryDate() != null) {
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String entryDate = dateFormat.format(product.getEntryDate());
            txtEntryDate.setText(entryDate);
        } else {
            txtEntryDate.setText(""); // Set to empty if EntryDate is null
        }
        
        // Notify user that product is found
        JOptionPane.showMessageDialog(this, "Product found!", "Success", JOptionPane.INFORMATION_MESSAGE);
    } else {
        // Notify user that product is not found
        JOptionPane.showMessageDialog(this, "Product not found!", "Error", JOptionPane.ERROR_MESSAGE);
    }        // TODO add your handling code here:
    }//GEN-LAST:event_btnFindActionPerformed

